/**
 * Generate an inline SVG element for a given icon name.
 * @param {string} name - Icon name from the ICONS map
 * @param {number} [size=16] - Width/height in pixels
 * @param {string} [className=''] - Additional CSS class
 * @returns {string} SVG HTML string
 */
export function icon(name, size = 16, className = '') {
  const paths = ICONS[name];
  if (!paths) {
    console.warn(`Icon not found: ${name}`);
    return `<span style="width:${size}px;height:${size}px;display:inline-block;">?</span>`;
  }
  return `<svg class="icon${className ? ' ' + className : ''}" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">${paths}</svg>`;
}

/**
 * Replace an element's text-based icon with an SVG icon.
 * Useful for swapping emoji icons on existing DOM elements.
 */
export function setIcon(el, name, size = 16) {
  if (!el) return;
  const span = el.querySelector('.ribbon-icon') || el.querySelector('.icon');
  if (span) {
    span.innerHTML = icon(name, size);
  }
}

